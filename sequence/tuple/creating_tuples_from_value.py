# Creating tuple from value
number = (1,2,3,4,5)
print("Type of number", type(number))
print("Value of number", number)

# Creatig an empty tuple

empty_tuple = tuple()
print("Type of empty_tuple", type(empty_tuple))
print("Value of empty_tuple", empty_tuple)

# Creatig tuple with one element

one_tuple = (1,)
print("Type of one_tuple", type(one_tuple))
print("Value of one_tuple", one_tuple)



