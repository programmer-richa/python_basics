# Creating tuple from value
number = (1,2,3,4,5)
print("Type of number", type(number))
print("Value of number", number)

# Accessing first element
first =number[0]
print("Type of first", type(first))
print("Value of first", first)

# Accessing last element
last =number[-1]
print("Type of last", type(last))
print("Value of last", last)
