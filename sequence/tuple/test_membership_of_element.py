# Creating tuple from value
number = (1,2,3,4,5)

print("Type of number", type(number))
print("Value of number", number)
if 2 in number:
    print("2 exists in the tuple")
else:
    print("2 does not exist in the tuple")

if 10 in number:
    print("10 exists in the tuple")
else:
    print("10 does not exist in the tuple")
