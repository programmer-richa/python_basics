# creating List from  values
whole_numbers = [0,1,2]
print("Type of whole_numbers", type(whole_numbers))
print("Value of whole_numbers", whole_numbers)
print("Extending list")
# adding multiple items to a list using extend method
whole_numbers.extend([3,4,5])
print("Type of whole_numbers", type(whole_numbers))
print("Value of whole_numbers", whole_numbers)
